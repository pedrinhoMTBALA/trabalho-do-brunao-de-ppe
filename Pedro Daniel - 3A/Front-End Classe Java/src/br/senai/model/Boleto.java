package br.senai.model;

public class Boleto {

    private String nomeb;
    private int foneb;
    private String emailb;
    private String cpfb;

    public String getNomeb() {
        return nomeb;
    }

    public void setNomeb(String nomeb) {
        this.nomeb = nomeb;
    }

    public int getFoneb() {
        return foneb;
    }

    public void setFoneb(int foneb) {
        this.foneb = foneb;
    }

    public String getEmailb() {
        return emailb;
    }

    public void setEmailb(String emailb) {
        this.emailb = emailb;
    }

    public String getCpfb() {
        return cpfb;
    }

    public void setCpfb(String cpfb) {
        this.cpfb = cpfb;
    }
}
